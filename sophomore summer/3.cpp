#include <bits/stdc++.h>
using namespace std;

int main() {
    ios::sync_with_stdio(false); // For faster input/output
    cin.tie(nullptr);

    int t;
    cin >> t;

    while (t--) {
        int n;
        cin >> n;

        if (n % 3 == 0) {
            cout << "Second" << endl;
        } else {
            cout << "First" << endl;
        }
    }

    return 0;
}
